//this functions takes a number as the value, calculates the square, and returns the result.
function squareNum($num)
{
    $sqnum = $num * $num;
    return $sqnum;
}

//This function takes a number as the value, calculates the number doubled, and returns the result.
function doubleNum($num)
{
    $doublednum = $num * 2;
    return $doublednum;
}

